void OLED_WrDat(uint8_t data)
{
    uint8_t i = 8;

    OLED_DC_OH;
    //asm ("nop");
    OLED_D0_OL;
    //asm ("nop"); 
    while (i --)
    {
        if (data & 0x80)
        {
            OLED_D1_OH;
        }
        else
        {
            OLED_D1_OL;
        }
        OLED_D0_OH; 
        //asm ("nop");
        //asm ("nop");
        OLED_D0_OL;    
        data <<= 1;    
    }
}

/*
 *  ÃüÁîÐ´Èë
 */
void OLED_WrCmd(uint8_t cmd)
{
    uint8_t i = 8;

    OLED_DC_OL;
    OLED_D0_OL;
    //asm ("nop");  
    while (i --)
    {
        if (cmd & 0x80)
        {
            OLED_D1_OH;
        }
        else
        {
            OLED_D1_OL;
        }
        OLED_D0_OH;
        //asm ("nop");
        //asm("nop");             
        OLED_D0_OL;    
        cmd <<= 1;  
    } 	
}

